class JavaMath {
public static void main(String x[]){
System.out.println("Absolute value of -5 is : "+Math.abs(-5));
System.out.println("Max of 5 and 7 is : "+Math.max(5,7));
System.out.println("Min of 5 and 7 is : "+Math.min(5,7));
System.out.println("Round value of 5.7 is : "+Math.round(5.7));
System.out.println("Cube root of 64 is : "+Math.cbrt(64));
System.out.println("5 to the power 7 is "+Math.pow(5,7));
}
}
